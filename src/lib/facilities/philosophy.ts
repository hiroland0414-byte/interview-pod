import type { ModeKey } from "@/lib/facilities/loader";

/**
 * 志望先の理念・方針要約（暫定版）
 * 本番では公式Webから取得・要約するロジックに差し替え予定。
 */
export async function getPhilosophySummary(
  facilityName: string,
  mode: ModeKey,
): Promise<string> {
  if (!facilityName) {
    return "志望先をまだ選んでいません。気になる病院や施設の公式Webサイトで、理念や方針を一度確認してみましょう。";
  }

  const role =
    mode === "A1"
      ? "診療放射線技師"
      : mode === "A2"
      ? "看護師"
      : mode === "B"
      ? "健診・クリニックスタッフ"
      : "医療関連企業の職員";

  // だいたい 400字前後をイメージしたテンプレ（本番は差し替え）
  return (
    `${facilityName} は、地域に根ざした医療提供と、患者さんとご家族に寄り添ったあたたかい対応を大切にしている医療機関です。` +
    `安全で質の高い検査・診療体制を維持しつつ、チーム医療の一員として多職種が連携しながら、継続的な医療サービスの向上を目指していると考えられます。` +
    `\n\n${role}として働くうえでは、専門技術の習得だけでなく、患者さんへのていねいな説明や、同僚と協力し合えるコミュニケーション力、` +
    `そして日々の業務の中で小さな改善を積み重ねていく姿勢が求められます。` +
    `この施設の理念や方針を意識しながら、「なぜ自分はここで働きたいのか」「どのように貢献できるのか」を志望動機に落とし込んでいくと、説得力のある自己PRにつながります。`
  );
}
